#!/usr/bin/env bun
/**
 * `bun run codegen:contracts` — emit the checked-in Rust serde mirror of the
 * `@nightcore/contracts` zod spine, killing silent TS↔Rust drift at the sidecar
 * (NDJSON stdio) boundary.
 *
 * The boundary is small and FULLY KNOWN, so this is a targeted emitter — NOT a
 * general-purpose zod→Rust compiler. It introspects exactly the constructs the
 * contract uses (discriminated unions of `z.object`s; `z.string/number/boolean`;
 * `z.literal`; `z.enum`; `z.optional`; `z.default`; `z.array`; `z.record`) and
 * refuses (throws) on anything it doesn't recognize, so a future contract change
 * that introduces an unhandled construct fails loudly here rather than emitting
 * wrong Rust.
 *
 * It drives off direct zod v4 schema introspection (`schema._zod.def`) rather than
 * `zod-to-json-schema` (which targets zod v3 and is only present transitively):
 * the int/unsigned/float distinction and the exact enum string values are read
 * straight from the schema, and the Rust conformance test is the proof.
 *
 * Outputs (both checked in):
 *   - apps/desktop/src-tauri/src/contracts/generated.rs   — the serde types.
 *   - apps/desktop/src-tauri/src/contracts/fixtures.json  — one wire payload per
 *       command/event variant, produced by PARSING a representative input through
 *       the zod schema (so the fixtures are tied to the zod source, defaults and
 *       all). The Rust conformance test loads these and asserts each deserializes.
 *
 * `--check` regenerates both in memory and diffs against the committed files,
 * exiting non-zero on any difference. This is the CI drift guard.
 */
