function registerInlineEnum(
  schema: AnyZod,
  fieldPath: string,
  ctx: EmitCtx,
): string {
  const d = def(schema);
  // zod v4 enum: `entries` is a value→value map; the wire strings are the values.
  const values: string[] = Object.values(d.entries ?? d.values ?? {});
  const sig = values.join('|');
  const existing = ctx.enumByValues.get(sig);
  if (existing) {
    return existing;
  }
  const name =
    ENUM_NAMES[sig] ?? `${pascal(fieldPath.replace(/\[\]/g, 'Item'))}Enum`;
  ctx.enumByValues.set(sig, name);

  // Choose the serde rename strategy that reproduces the exact wire strings.
  // If every value already matches Rust's PascalCase→? we still emit explicit
  // per-variant renames when a single container rule can't reproduce them — but
  // for these contracts a `rename_all` rule always fits, so detect it.
  const rename = detectRenameAll(values);
  const variants = values
    .map((v) => {
      const ident = pascal(v);
      if (rename) {
        return `    ${ident},`;
      }
      return `    #[serde(rename = ${JSON.stringify(v)})]\n    ${ident},`;
    })
    .join('\n');
  const attrs = ['#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]'];
  if (rename) {
    attrs.push(`#[serde(rename_all = ${JSON.stringify(rename)})]`);
  }
  const decl = `${attrs.join('\n')}\npub enum ${name} {\n${variants}\n}`;
  ctx.decls.set(name, decl);
  return name;
}

/** Find a serde `rename_all` rule that maps each PascalCase variant ident back to
 *  its exact wire string. Returns the rule name, or `null` if none fits (then the
 *  emitter falls back to per-variant `#[serde(rename = "…")]`). */
function detectRenameAll(values: string[]): string | null {
  const candidates: Array<[string, (ident: string) => string]> = [
    ['lowercase', (id) => id.toLowerCase()],
    ['camelCase', (id) => id[0]!.toLowerCase() + id.slice(1)],
    [
      'kebab-case',
      (id) => id.replace(/([a-z0-9])([A-Z])/g, '$1-$2').toLowerCase(),
    ],
    [
      'snake_case',
      (id) => id.replace(/([a-z0-9])([A-Z])/g, '$1_$2').toLowerCase(),
    ],
  ];
  for (const [rule, apply] of candidates) {
    if (values.every((v) => apply(pascal(v)) === v)) {
      return rule;
    }
  }
  return null;
}

function registerInlineStruct(
  schema: AnyZod,
  fieldPath: string,
  ctx: EmitCtx,
): string {
  // Prefer a canonical name keyed by the struct's field-key signature, so a struct
  // referenced from several fields (the SDK `SessionInfo` in `sessions[]` and
  // `info`) collapses to one named type — and dedupes to the same prior emit.
  const d = def(schema);
  const shape = typeof d.shape === 'function' ? d.shape() : d.shape;
  const sig = Object.keys(shape).sort().join('|');
  const existing = ctx.structByFields.get(sig);
  if (existing) {
    return existing;
  }
  const name = STRUCT_NAMES[sig] ?? pascal(fieldPath.replace(/\[\]/g, 'Item'));
  if (ctx.decls.has(name)) {
    ctx.structByFields.set(sig, name);
    return name;
  }
  ctx.structByFields.set(sig, name);
  // Reserve the name before recursing so a self-reference can't loop.
  ctx.decls.set(name, '');
  const fields = emitObjectFields(schema, `${name}`, ctx, undefined, true);
  const decl =
    '#[derive(Debug, Clone, Serialize, Deserialize)]\n' +
    '#[serde(rename_all = "camelCase")]\n' +
    `pub struct ${name} {\n${fields}\n}`;
  ctx.decls.set(name, decl);
  return name;
}

/** Emit the Rust struct fields for a `z.object`, one per shape key (the literal
 *  discriminator key, if any, is skipped — it lives in the enum tag). */
function emitObjectFields(
  objectSchema: AnyZod,
  ownerName: string,
  ctx: EmitCtx,
  discriminator?: string,
  pubFields = false,
): string {
  const d = def(objectSchema);
  const shape = typeof d.shape === 'function' ? d.shape() : d.shape;
  const lines: string[] = [];
  for (const [wireKey, fieldSchema] of Object.entries(shape) as Array<
    [string, AnyZod]
  >) {
    const fd = def(fieldSchema as AnyZod);
    if (fd.type === 'literal') {
      // The discriminator field — represented by the serde enum tag, not a field.
      // It must be the union's discriminator key (a stray literal is a bug).
      if (discriminator !== undefined && wireKey !== discriminator) {
        throw new Error(
          `unexpected literal field "${wireKey}" in ${ownerName}; only the union discriminator may be a literal`,
        );
      }
      continue;
    }
    const { inner, optional, hasDefault, nullable } = unwrap(fieldSchema as AnyZod);
    const fieldPath = `${ownerName}_${wireKey}`;
    const baseType = rustType(inner, fieldPath, ctx);
    const rustField = snake(wireKey);
    const attrs: string[] = [];
    // serde renames the field back to its camelCase wire key. `#[serde(rename_all
    // = "camelCase")]` on the container handles the common case, but snake→camel
    // is lossy for keys like `sdkSessionId` (→ `sdk_session_id` → `sdkSessionId`?
    // serde's camelCase gives `sdkSessionId` ✓). Still, emit an explicit rename
    // whenever the container rule wouldn't reproduce the key, to be exact.
    if (camelCase(rustField) !== wireKey) {
      attrs.push(`rename = ${JSON.stringify(wireKey)}`);
    }
    let ty: string;
    if (optional) {
      // `z.optional()` → omit when absent (matches the current `json!` output and
      // the zod `.optional()` side, which rejects/differs on an explicit null).
      // An optional-AND-nullable field is still omitted when absent, and an
      // explicit `null` deserializes to `None` (serde maps both absent and null
      // to `None` for `Option<T>`), so the same attrs cover both.
      ty = `Option<${baseType}>`;
      attrs.unshift('skip_serializing_if = "Option::is_none"');
      attrs.unshift('default');
    } else if (nullable) {
      // `z.nullable()` (required, present, may be `null`) → `Option<T>` that is
      // ALWAYS serialized: `None` → `null`, `Some(v)` → `v`. No
      // `skip_serializing_if`, so the key is never omitted — matching the wire
      // shape of a `T | null` field (e.g. `parentToolUseId`, a cleared `tag`).
      ty = `Option<${baseType}>`;
      attrs.unshift('default');
    } else if (hasDefault) {
      // `z.default(x)` → tolerant on deserialize; the value is guaranteed present
      // after the default, so keep the type non-Option.
      ty = baseType;
      attrs.unshift('default');
    } else {
      ty = baseType;
    }
    if (attrs.length > 0) {
      lines.push(`    #[serde(${attrs.join(', ')})]`);
    }
    // Fields inside an enum struct-variant are implicitly public; `pub` there is a
    // syntax error. Only named `struct` fields carry `pub`. A keyword ident (e.g.
    // `type`) is escaped to a raw ident (`r#type`); serde strips `r#`, so the
    // container `rename_all`/explicit rename still reproduces the wire key.
    const vis = pubFields ? 'pub ' : '';
    lines.push(`    ${vis}${escapeRustIdent(rustField)}: ${ty},`);
  }
  return lines.join('\n');
}

/** snake_case Rust ident → the camelCase serde would produce, to decide whether
 *  the container `rename_all = "camelCase"` already reproduces the wire key. */
function camelCase(s: string): string {
  return s.replace(/_([a-z0-9])/g, (_m, c: string) => c.toUpperCase());
}

// ---------------------------------------------------------------------------
// Tagged-union emitter (SurfaceCommand / NightcoreEvent)
// ---------------------------------------------------------------------------

interface VariantInfo {
  /** The wire `type` literal (kebab-case), e.g. `start-session`. */
  tag: string;
  /** The Rust variant ident, e.g. `StartSession`. */
  ident: string;
  /** The variant's struct fields (Rust source, already indented). */
  fields: string;
}

/** Render an internally-tagged Rust enum for a zod discriminated union. Shared by
 *  the top-level `type`-tagged unions (SurfaceCommand / NightcoreEvent) and any
 *  nested union (e.g. `PermissionDecision`, tagged by `behavior`). `discriminator`
 *  is the wire key carrying the variant literal; `tagRule` is the serde
 *  `rename_all` that maps the Rust variant ident back to that literal. */
function renderUnionEnum(
  unionSchema: AnyZod,
  enumName: string,
  discriminator: string,
  ctx: EmitCtx,
): string {
  const d = def(unionSchema);
  if (d.type !== 'union') {
    throw new Error(`${enumName} is not a discriminated union`);
  }
  const variants: VariantInfo[] = [];
  const tags: string[] = [];
  for (const option of d.options as AnyZod[]) {
    const od = def(option);
    const shape = typeof od.shape === 'function' ? od.shape() : od.shape;
    const tagField = shape[discriminator];
    if (!tagField || def(tagField).type !== 'literal') {
      throw new Error(
        `union ${enumName} has an option without a literal "${discriminator}"`,
      );
    }
    const tag: string = def(tagField).values[0];
    tags.push(tag);
    const ident = pascal(tag);
    const fields = emitObjectFields(option, ident, ctx, discriminator);
    variants.push({ tag, ident, fields });
  }

  // The container `rename_all` must reproduce every variant literal exactly; fall
  // back to per-variant `#[serde(rename = "…")]` if no single rule fits.
  const tagRule = detectRenameAll(tags);
  const renderedVariants = variants
    .map((v) => {
      const body = v.fields.trim().length === 0 ? '' : `\n${v.fields}\n    `;
      const renameAttr = tagRule
        ? ''
        : `    #[serde(rename = ${JSON.stringify(v.tag)})]\n`;
      return `${renameAttr}    #[serde(rename_all = "camelCase")]\n    ${v.ident} {${body}},`;
    })
    .join('\n');

  const containerAttrs = [`tag = ${JSON.stringify(discriminator)}`];
  if (tagRule) {
    containerAttrs.push(`rename_all = ${JSON.stringify(tagRule)}`);
  }
  return (
    '#[derive(Debug, Clone, Serialize, Deserialize)]\n' +
    `#[serde(${containerAttrs.join(', ')})]\n` +
    `pub enum ${enumName} {\n${renderedVariants}\n}`
  );
}

/** A nested discriminated union referenced from a field (e.g. the `decision`
 *  field's `PermissionDecision`). Registered as a named enum and referenced by
 *  name. The Rust name comes from the canonical registry keyed by the variant
 *  tag set, falling back to the field path. */
function registerInlineUnion(
  schema: AnyZod,
  fieldPath: string,
  ctx: EmitCtx,
): string {
  const d = def(schema);
  const tags = (d.options as AnyZod[]).map((o) => {
    const od = def(o);
    const shape = typeof od.shape === 'function' ? od.shape() : od.shape;
    const disc = d.discriminator as string;
    return def(shape[disc]).values[0] as string;
  });
  const sig = `${d.discriminator}:${tags.join('|')}`;
  const existing = ctx.unionByTags.get(sig);
  if (existing) {
    return existing;
  }
  const name = UNION_NAMES[sig] ?? `${pascal(fieldPath.replace(/\[\]/g, 'Item'))}Union`;
  ctx.unionByTags.set(sig, name);
  // Reserve the name before recursing so a self-reference can't loop.
  ctx.decls.set(name, '');
  const decl = renderUnionEnum(schema, name, d.discriminator as string, ctx);
  ctx.decls.set(name, decl);
  return name;
}

function emitTaggedUnion(
  unionSchema: AnyZod,
  enumName: string,
  ctx: EmitCtx,
): string {
  return renderUnionEnum(unionSchema, enumName, 'type', ctx);
}

