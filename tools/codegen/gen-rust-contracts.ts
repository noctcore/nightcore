// ---------------------------------------------------------------------------
// zod v4 introspection helpers
// ---------------------------------------------------------------------------

type AnyZod = z.ZodType;

/** The internal `def` for a zod v4 schema (`_zod.def`), where every kind/shape
 *  lives. We read this directly rather than via a JSON-schema bridge so the
 *  numeric and enum distinctions survive intact. */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function def(schema: AnyZod): any {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const s = schema as any;
  return s?._zod?.def ?? s?._def;
}

/** Unwrap `z.optional()` / `z.default()` / `z.nullable()` to the inner schema,
 *  recording the modifiers seen so the field emitter can choose
 *  `Option<T>` / `#[serde(default)]`. */
interface Unwrapped {
  inner: AnyZod;
  optional: boolean;
  hasDefault: boolean;
  /** `z.nullable()` — present on the wire but may be `null`. Distinct from
   *  `optional` (absent): a nullable field serializes `None` as `null` and is
   *  never omitted, so it maps to `Option<T>` WITHOUT `skip_serializing_if`. */
  nullable: boolean;
}

function unwrap(schema: AnyZod): Unwrapped {
  let cur = schema;
  let optional = false;
  let hasDefault = false;
  let nullable = false;
  // Optional/default/nullable can nest (e.g. `.optional()` over a `.nullable()`);
  // peel all three.
  for (;;) {
    const d = def(cur);
    if (d.type === 'optional') {
      optional = true;
      cur = d.innerType;
    } else if (d.type === 'default' || d.type === 'prefault') {
      hasDefault = true;
      cur = d.innerType;
    } else if (d.type === 'nullable') {
      nullable = true;
      cur = d.innerType;
    } else {
      break;
    }
  }
  return { inner: cur, optional, hasDefault, nullable };
}

/** Map a (already-unwrapped) zod leaf/enum/array/record schema to a Rust type
 *  name, registering any enum it references so the emitter declares it once. */
function rustType(schema: AnyZod, fieldPath: string, ctx: EmitCtx): string {
  const d = def(schema);
  switch (d.type) {
    case 'string':
      return 'String';
    case 'boolean':
      return 'bool';
    case 'number':
      return numberRustType(d, fieldPath);
    case 'literal': {
      // A literal field is the discriminator; handled by the enum emitter, never
      // as a struct field. Reaching here means an unexpected literal.
      throw new Error(
        `unexpected literal at ${fieldPath}; literals are only supported as the union discriminator`,
      );
    }
    case 'enum':
      return registerInlineEnum(schema, fieldPath, ctx);
    case 'array': {
      const elem = rustType(unwrap(d.element).inner, `${fieldPath}[]`, ctx);
      return `Vec<${elem}>`;
    }
    case 'record':
      // `z.record(z.string(), z.unknown())` → an arbitrary JSON object. Match the
      // reader, which treats these as opaque forwarded JSON.
      return 'serde_json::Map<String, serde_json::Value>';
    case 'object':
      // A nested object (e.g. the `usage` token-usage struct). Emit it as a named
      // struct and reference it by name.
      return registerInlineStruct(schema, fieldPath, ctx);
    case 'union':
      // A nested discriminated union (e.g. `PermissionDecision`, tagged by
      // `behavior`). Emit it as a named internally-tagged Rust enum.
      return registerInlineUnion(schema, fieldPath, ctx);
    default:
      throw new Error(
        `unsupported zod type "${d.type}" at ${fieldPath}; the emitter handles only the known contract constructs`,
      );
  }
}

/** The `z.number()` check kinds this emitter reads via zod's internal AST. Any
 *  check kind OUTSIDE this set means the introspection contract shifted (almost
 *  always a zod dependency bump reshaping `_zod.def.checks`) and int/float
 *  inference can no longer be trusted — see `numberRustType` for why that must
 *  throw rather than fall through. Extend this set (and the logic below) when a
 *  new numeric constraint is intentionally introduced to the contract. */
const KNOWN_NUMBER_CHECKS = new Set([
  'number_format', // `.int()` — carries the `safeint`/`uint*` format tag
  'greater_than', // `.positive()` / `.nonnegative()` / `.min()` lower bound
  'greater_than_or_equal', // tolerated: a future zod shape for an inclusive `>=`
  'less_than', // `.max()` upper bound — unused today, tolerated
  'less_than_or_equal',
  'multiple_of', // `.multipleOf()` / `.step()` — unused today, tolerated
]);

/** Pick the Rust numeric type from a `z.number()`'s checks: a safe-int with a
 *  `>=0` / `>0` bound → `u64` (the reader uses
 *  `as_u64`); a safe-int without a non-negative bound → `i64`; a plain number
 *  (no int format) → `f64`.
 *
 *  Fails LOUD on any UNRECOGNIZED check shape. This is the crux: JSON has no
 *  int/float distinction, so a `u64 → f64` regression deserializes the same
 *  fixture bytes fine and sails past the Rust round-trip conformance test. A
 *  silent `if (!isInt) return 'f64'` fallback would therefore let a zod-internals
 *  reshape downgrade every bounded integer to `f64` undetected. We distinguish an
 *  ABSENT check set (a genuine plain `z.number()` → f64, intentional) from an
 *  UNRECOGNIZED one (the drift signal → throw). The `gen-rust-contracts.test.ts`
 *  canary additionally locks the u64/i64/f64 mapping so a reshape that RELOCATES
 *  `.checks` (leaving it empty) also fails a test rather than silently drifting. */
// eslint-disable-next-line @typescript-eslint/no-explicit-any
export function numberRustType(d: any, fieldPath: string): string {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const checks: any[] = (d.checks ?? []).map((c: any) => c?._zod?.def ?? c);
  for (const c of checks) {
    if (typeof c?.check !== 'string' || !KNOWN_NUMBER_CHECKS.has(c.check)) {
      throw new Error(
        `unrecognized z.number() check ${JSON.stringify(
          c?.check,
        )} at ${fieldPath}: zod's internal check AST may have changed. Review ` +
          `numberRustType()/KNOWN_NUMBER_CHECKS in tools/codegen/gen-rust-contracts.ts ` +
          `rather than let a bounded integer silently degrade to f64.`,
      );
    }
  }
  const isInt = checks.some(
    (c) => c.check === 'number_format' && String(c.format).includes('int'),
  );
  if (!isInt) {
    // Reached only after every present check was recognized above, so this is a
    // genuine floating-point wire value (costUsd, confidence, timestamps) → f64.
    return 'f64';
  }
  const nonNegative = checks.some(
    (c) =>
      (c.check === 'greater_than' || c.check === 'greater_than_or_equal') &&
      c.value === 0,
  );
  void fieldPath;
  return nonNegative ? 'u64' : 'i64';
}

// ---------------------------------------------------------------------------
// Rust identifier helpers
// ---------------------------------------------------------------------------

/** kebab-or-snake string → PascalCase Rust type/variant ident. */
function pascal(s: string): string {
  return s
    .split(/[-_\s]+/)
    .filter(Boolean)
    .map((p) => p[0]!.toUpperCase() + p.slice(1))
    .join('');
}

/** camelCase wire key → snake_case Rust field ident. */
function snake(s: string): string {
  return s
    .replace(/([a-z0-9])([A-Z])/g, '$1_$2')
    .replace(/[-\s]+/g, '_')
    .toLowerCase();
}

/** Rust 2021 strict + reserved keywords that can appear as a snake_case field
 *  ident (e.g. a `type` wire key). serde strips the `r#` prefix, so a raw-ident
 *  field still serializes under the container's `rename_all` rule. */
const RUST_KEYWORDS = new Set([
  'as', 'break', 'const', 'continue', 'crate', 'dyn', 'else', 'enum', 'extern',
  'false', 'fn', 'for', 'if', 'impl', 'in', 'let', 'loop', 'match', 'mod', 'move',
  'mut', 'pub', 'ref', 'return', 'self', 'static', 'struct', 'super', 'trait',
  'true', 'type', 'unsafe', 'use', 'where', 'while', 'async', 'await',
]);

/** Escape a snake_case field ident that collides with a Rust keyword to a raw
 *  identifier (`r#type`). `crate`/`self`/`super`/`Self` can't be raw idents, so a
 *  trailing underscore is used for those — none appear in the contract today, but
 *  the guard keeps a future field from emitting invalid Rust. */
function escapeRustIdent(ident: string): string {
  if (!RUST_KEYWORDS.has(ident)) return ident;
  if (ident === 'crate' || ident === 'self' || ident === 'super') {
    return `${ident}_`;
  }
  return `r#${ident}`;
}

