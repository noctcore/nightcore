/** The `@nightcore/contracts` `CHANNELS` registry as a stable, sorted list of
 *  `[registrySymbol, wireName]` pairs. Emitted into `generated.rs` as
 *  `NIGHTCORE_CHANNELS`; the Rust `contracts/mod.rs` conformance test asserts each
 *  scattered `*_EVENT` const equals its entry, so a channel renamed on either tier
 *  reds `cargo test`. Exported for the codegen unit test. */
export function buildChannels(): Array<[string, string]> {
  return Object.entries(CHANNELS).sort(([a], [b]) => a.localeCompare(b));
}

/** Render the `NIGHTCORE_CHANNELS` const from the sorted channel pairs. */
function renderChannels(entries: Array<[string, string]>): string {
  const rows = entries
    .map(([sym, name]) => `    (${JSON.stringify(sym)}, ${JSON.stringify(name)}),`)
    .join('\n');
  return [
    '/// Every `nc:*` Tauri event channel name, keyed by its `@nightcore/contracts`',
    '/// `CHANNELS` registry symbol — the single source. The conformance test in',
    '/// `contracts/mod.rs` asserts each scattered Rust `*_EVENT` const equals its',
    '/// entry here, so a channel renamed/added/removed on either tier fails',
    '/// `cargo test` (and a rename in the zod source also reds `codegen-drift`).',
    'pub const NIGHTCORE_CHANNELS: &[(&str, &str)] = &[',
    rows,
    '];',
  ].join('\n');
}

// ---------------------------------------------------------------------------
// Top-level emit
// ---------------------------------------------------------------------------

function emitRust(): string {
  const ctx: EmitCtx = {
    decls: new Map(),
    enumByValues: new Map(),
    enumNameToValues: new Map(),
    unionByTags: new Map(),
    structByFields: new Map(),
  };
  // Static guard on the ENUM_NAMES registry itself: it must be an injection (each
  // canonical Rust name backed by exactly one value-set), so a duplicate name can't
  // silently collapse two value-sets before emission even begins.
  assertEnumNamesInjective();

  // Emit the three tagged unions first; field emission populates `ctx.decls` with
  // every referenced enum and nested struct as a side effect.
  const surface = emitTaggedUnion(SurfaceCommandSchema, 'SurfaceCommand', ctx);
  const query = emitTaggedUnion(SurfaceQuerySchema, 'SurfaceQuery', ctx);
  const event = emitTaggedUnion(NightcoreEventSchema, 'NightcoreEvent', ctx);

  // Supporting types (enums + nested structs) declared in a stable order so the
  // output is deterministic regardless of discovery order.
  const supporting = [...ctx.decls.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([, decl]) => decl)
    .join('\n\n');

  const body = [
    GENERATED_HEADER,
    '//',
    '// Rust serde mirror of the `@nightcore/contracts` zod spine (commands.ts +',
    '// events.ts and the enums they reference). Regenerate with',
    '// `bun run codegen:contracts`; CI guards drift with `--check` and the',
    '// `cargo test` conformance suite in `contracts/mod.rs`.',
    '#![allow(dead_code)]',
    '// The wire unions are deserialize/forward shapes; a large reply variant (e.g.',
    '// the provider-config snapshot) is fine here — boxing would only complicate the',
    '// serde round-trip these mirrors exist to preserve.',
    '#![allow(clippy::large_enum_variant)]',
    '',
    'use serde::{Deserialize, Serialize};',
    '',
    '// === Surface → engine commands (Rust SERIALIZES these) ===',
    '',
    surface,
    '',
    '// === Surface → engine queries (Rust SERIALIZES these; replies arrive as the',
    '//     `query-result` NightcoreEvent the reader correlates by requestId) ===',
    '',
    query,
    '',
    '// === Engine → surface events (Rust DESERIALIZES / forwards these) ===',
    '',
    event,
    '',
    '// === Referenced enums and nested structs ===',
    '',
    supporting,
    '',
    '// === Event channel registry (nc:*) — single-sourced from `CHANNELS`; the',
    '//     conformance test in `contracts/mod.rs` ties every `*_EVENT` const to it ===',
    '',
    renderChannels(buildChannels()),
    '',
  ].join('\n');

  return body;
}

