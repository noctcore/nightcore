/** Format Rust source via `rustfmt` so the checked-in file is fmt-clean and the
 *  `--check` diff is stable. If `rustfmt` is unavailable, emit unformatted (the
 *  `cargo fmt --check` gate would then catch it) — but it is part of the Rust
 *  toolchain and present in CI. */
function formatRust(src: string): string {
  try {
    return execFileSync('rustfmt', ['--emit', 'stdout', '--edition', '2021'], {
      input: src,
      encoding: 'utf8',
      maxBuffer: 16 * 1024 * 1024,
    });
  } catch (error) {
    process.stderr.write(
      `warning: rustfmt unavailable or failed (${String(
        error,
      )}); emitting unformatted Rust\n`,
    );
    return src;
  }
}

function renderFixtures(fixtures: Fixtures): string {
  return `${JSON.stringify(fixtures, null, 2)}\n`;
}

function generate(): { rust: string; fixtures: string } {
  const rust = formatRust(emitRust());
  const fixtures = renderFixtures(buildFixtures());
  return { rust, fixtures };
}

function main(): void {
  const check = process.argv.includes('--check');
  const { rust, fixtures } = generate();

  if (check) {
    let drift = false;
    for (const [file, next] of [
      [RUST_OUT, rust],
      [FIXTURES_OUT, fixtures],
    ] as const) {
      const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : '';
      if (current !== next) {
        drift = true;
        process.stderr.write(
          `drift: ${path.relative(REPO_ROOT, file)} is out of date — run \`bun run codegen:contracts\`\n`,
        );
      }
    }
    if (drift) {
      process.exit(1);
    }
    process.stdout.write('contracts codegen: in sync\n');
    return;
  }

  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.writeFileSync(RUST_OUT, rust);
  fs.writeFileSync(FIXTURES_OUT, fixtures);
  process.stdout.write(
    `wrote ${path.relative(REPO_ROOT, RUST_OUT)} and ${path.relative(
      REPO_ROOT,
      FIXTURES_OUT,
    )}\n`,
  );
}

main();
